const recommendationReducer = (state = recommendations, action) => {
  // TODO: Move the logic to the action
  // console.log('recommendationsReducer go now!', 'state =', state, 'action =', action);

  switch (action.type) {
    case C.ADD_RECOMMENDATION:
      console.log('ADD_RECOMMENDATION state =', state);
      const addArray = recommendations.map((r) => {
        const rname = action.payload;
        if (r.name === rname) {
          // eslint-disable-next-line no-param-reassign
          r.selected = !r.selected;
          return r;
        }
        return r;
      });
      return addArray;
    case C.ADD_SUB_RECOMMENDATION:
      console.log('ADD_SUB_RECOMMENDATION state =', state);
      const addSubArray = recommendations.map((r) => {
        r.recommendation.map((sr) => {
          const rSubName = action.payload;
          if (sr.id === rSubName) {
            // eslint-disable-next-line no-param-reassign
            sr.selected = !sr.selected;
            return sr;
          }
          return sr;
        });
        return r;
      });
      return addSubArray;
    default:
      return state;
  }

  // return action.type === C.ADD_RECOMMENDATION ? array : state;
};

export default recommendationReducer;
